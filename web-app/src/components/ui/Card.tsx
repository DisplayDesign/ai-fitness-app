import React from 'react';
import { cn } from '@/lib/utils';
import { customClasses } from '@/lib/theme';

interface CardProps extends React.HTMLAttributes<HTMLDivElement> {
  variant?: 'primary' | 'gradient' | 'dark';
  children: React.ReactNode;
}

export function Card({ variant = 'primary', className, children, ...props }: CardProps) {
  const variantClasses = {
    primary: customClasses.card.primary,
    gradient: customClasses.card.gradient,
    dark: customClasses.card.dark,
  };

  return (
    <div
      className={cn(variantClasses[variant], className)}
      {...props}
    >
      {children}
    </div>
  );
}

interface CardHeaderProps extends React.HTMLAttributes<HTMLDivElement> {
  children: React.ReactNode;
}

export function CardHeader({ className, children, ...props }: CardHeaderProps) {
  return (
    <div
      className={cn('flex flex-col space-y-1.5 p-6', className)}
      {...props}
    >
      {children}
    </div>
  );
}

interface CardTitleProps extends React.HTMLAttributes<HTMLHeadingElement> {
  children: React.ReactNode;
}

export function CardTitle({ className, children, ...props }: CardTitleProps) {
  return (
    <h3
      className={cn('text-2xl font-semibold leading-none tracking-tight text-gradient-primary', className)}
      {...props}
    >
      {children}
    </h3>
  );
}

